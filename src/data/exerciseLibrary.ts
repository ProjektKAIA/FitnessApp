// Helper Funktionen
export const getExerciseById = (id: string): ExerciseDetail | undefined => {
  return EXERCISE_LIBRARY.find((e) => e.id === id);
};

export const getExercisesByMuscleGroup = (muscleGroup: TMuscleGroup): ExerciseDetail[] => {
  return EXERCISE_LIBRARY.filter((e) => e.muscleGroup === muscleGroup);
};

export const getExercisesByDifficulty = (difficulty: ExerciseDetail['difficulty']): ExerciseDetail[] => {
  return EXERCISE_LIBRARY.filter((e) => e.difficulty === difficulty);
};

export const getExercisesByEquipment = (equipment: string): ExerciseDetail[] => {
  return EXERCISE_LIBRARY.filter((e) => e.equipment.includes(equipment));
};
